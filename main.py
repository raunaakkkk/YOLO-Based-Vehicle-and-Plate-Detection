def detect_image(image_path):
    img = cv2.imread(image_path)
    if img is None: raise FileNotFoundError(image_path)
    H, W = img.shape[:2]
    detections = []
    v_out = vehicle_model(img, imgsz=IMG_SIZE,
                          classes=list(VEHICLE_CLASSES.keys()),
                          conf=0.15, verbose=False)[0]
    if len(v_out.boxes) == 0:
        p_out = plate_model(img, imgsz=IMG_SIZE, conf=0.10, verbose=False)[0]
        plates = []
        for pb in p_out.boxes:
            px1,py1,px2,py2 = map(int, pb.xyxy[0].tolist())
            plate_img = img[py1:py2, px1:px2]
            text = read_plate(plate_img)
            plates.append({"plate_bbox":[px1,py1,px2,py2],
                           "plate_conf":float(pb.conf[0]),
                           "plate_text":text,
                           "plate_length":len(text),
                           "valid_format":is_valid_plate(text),
                           "matched_formats":matched_formats(text)})
        return {"image":image_path, "num_vehicles":0, "detections":[{"plates":plates}]}
    for vb in v_out.boxes:
        vx1,vy1,vx2,vy2 = map(int, vb.xyxy[0].tolist())
        v_crop = img[vy1:vy2, vx1:vx2]
        p_out = plate_model(v_crop, imgsz=IMG_SIZE, conf=0.10, verbose=False)[0]
        plates = []
        for pb in p_out.boxes:
            px1,py1,px2,py2 = map(int, pb.xyxy[0].tolist())
            plate_img = v_crop[py1:py2, px1:px2]
            text = read_plate(plate_img)
            plates.append({"plate_bbox":[vx1+px1,vy1+py1,vx1+px2,vy1+py2],
                           "plate_conf":float(pb.conf[0]),
                           "plate_text":text,
                           "plate_length":len(text),
                           "valid_format":is_valid_plate(text),
                           "matched_formats":matched_formats(text)})
        detections.append({"vehicle_class":VEHICLE_CLASSES.get(int(vb.cls[0]),"vehicle"),
                           "vehicle_bbox":[vx1,vy1,vx2,vy2],
                           "vehicle_conf":float(vb.conf[0]),
                           "plates":plates})
    return {"image":image_path, "num_vehicles":len(detections), "detections":detections}

