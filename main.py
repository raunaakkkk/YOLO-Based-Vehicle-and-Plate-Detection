VEHICLE_CLASSES = {2: "car", 3: "motorcycle", 5: "bus", 7: "truck"}

PLATE_FORMATS = {
    "indian_full":   r"^[A-Z]{2}\d{1,2}[A-Z]{1,3}\d{1,4}$",
    "indian_short":  r"^[A-Z]{2,3}\d{1,4}$",
    "vietnam_car":   r"^\d{2}[A-Z]\d{3}\d{2,3}$",
    "vietnam_bike":  r"^\d{2}[A-Z]\d?\d{4,5}$",
    "european":      r"^[A-Z]{1,3}\d{1,4}[A-Z]{0,3}$",
    "us":            r"^[A-Z0-9]{5,8}$",
    "generic":       r"^[A-Z0-9]{4,12}$",
}
PLATE_REGEXES = {k: re.compile(v) for k, v in PLATE_FORMATS.items()}
MIN_PLATE_LEN, MAX_PLATE_LEN = 4, 12

def is_valid_plate(s):
    if not s or not (MIN_PLATE_LEN <= len(s) <= MAX_PLATE_LEN): return False
    return any(rx.match(s) for rx in PLATE_REGEXES.values())

def matched_formats(s):
    return [name for name, rx in PLATE_REGEXES.items() if rx.match(s or "")]

def clean_text(s):
    return re.sub(r"[^A-Z0-9]", "", (s or "").upper())

# --- 7. PREP + DESKEW + REC-ONLY FAST PATH + TWO-LINE HANDLING ---
def deskew_plate(crop):
    """Strict deskew: only fires when we have STRONG, CONSISTENT line evidence."""
    if crop is None or crop.size == 0: return crop
    h, w = crop.shape[:2]
    if h < 30 or w < 60: return crop
    gray = cv2.cvtColor(crop, cv2.COLOR_BGR2GRAY)
    edges = cv2.Canny(gray, 50, 150)
    lines = cv2.HoughLinesP(edges, 1, np.pi/180,
                            threshold=max(w//6, 30),
                            minLineLength=max(w//2, 40),
                            maxLineGap=5)
    if lines is None or len(lines) < 3: return crop
    angles = [np.degrees(np.arctan2(y2-y1, x2-x1))
              for x1,y1,x2,y2 in lines[:,0]
              if abs(np.degrees(np.arctan2(y2-y1, x2-x1))) < 20]
    if len(angles) < 3: return crop
    if np.std(angles) > 5: return crop
    angle = float(np.median(angles))
    if abs(angle) < 1.5 or abs(angle) > 12: return crop
    cos, sin = abs(np.cos(np.radians(angle))), abs(np.sin(np.radians(angle)))
    new_w = int(h*sin + w*cos); new_h = int(h*cos + w*sin)
    M = cv2.getRotationMatrix2D((w/2, h/2), angle, 1.0)
    M[0,2] += (new_w - w) / 2
    M[1,2] += (new_h - h) / 2
    return cv2.warpAffine(crop, M, (new_w, new_h),
                          flags=cv2.INTER_CUBIC,
                          borderMode=cv2.BORDER_REPLICATE)

def prep_plate(crop):
    if crop is None or crop.size == 0: return None
    h, w = crop.shape[:2]
    dy, dx = int(h * 0.04), int(w * 0.02)
    tight = crop[dy:max(h-dy, dy+1), dx:max(w-dx, dx+1)]
    if tight.size == 0: tight = crop
    th = tight.shape[0]
    if th < 56:
        s = 56 / th
        tight = cv2.resize(tight, None, fx=s, fy=s, interpolation=cv2.INTER_CUBIC)
    lab = cv2.cvtColor(tight, cv2.COLOR_BGR2LAB)
    l, a, b = cv2.split(lab)
    l = cv2.createCLAHE(clipLimit=3.0, tileGridSize=(8,8)).apply(l)
    return cv2.cvtColor(cv2.merge([l, a, b]), cv2.COLOR_LAB2BGR)

def _parse_rec_only(result):
    if not result: return ""
    texts = []
    for r in result:
        if isinstance(r, (list, tuple)):
            if r and isinstance(r[0], (list, tuple)):
                texts.append(str(r[0][0]) if r[0] else "")
            else:
                texts.append(str(r[0]) if r else "")
        else:
            texts.append(str(r))
    return clean_text("".join(texts))

def _parse_det_rec(result, force_concat=False):
    if not result: return ""
    items = []
    for r in result:
        if not (isinstance(r, (list, tuple)) and len(r) >= 2): continue
        box, txt = r[0], r[1]
        if isinstance(txt, (list, tuple)): txt = txt[0]
        if box and isinstance(box, list) and len(box) >= 1:
            ys = [p[1] for p in box]; xs = [p[0] for p in box]
            items.append((float(np.mean(ys)), float(np.mean(xs)), str(txt)))
        else:
            items.append((0.0, 0.0, str(txt)))
    if not items: return ""
    if force_concat:
        items.sort(key=lambda x: (round(x[0]/20), x[1]))
    else:
        items.sort(key=lambda x: x[1])
    return clean_text("".join(t for _,_,t in items))

def _read_two_line(crop):
    h, w = crop.shape[:2]
    top = crop[:h//2 + 2, :]
    bot = crop[h//2 - 2:, :]
    out = ""
    for part in (top, bot):
        prep = prep_plate(part)
        if prep is None: continue
        try:
            r, _ = ocr(prep, use_det=False, use_cls=False, use_rec=True)
            out += _parse_rec_only(r)
        except Exception: pass
    return out

def read_plate(plate_img):
    if plate_img is None or plate_img.size == 0: return ""
    # pad the crop ~6% so we don't lose edge chars after deskew/warp
    h0, w0 = plate_img.shape[:2]
    py, px = int(h0*0.06), int(w0*0.06)
    plate_img = cv2.copyMakeBorder(plate_img, py, py, px, px,
                                    cv2.BORDER_REPLICATE)
    plate_img = deskew_plate(plate_img)

    h, w = plate_img.shape[:2]
    aspect = h / max(w, 1)
    is_two_line = aspect > 0.40

    def _accept(s):
        """Accept only plausible plate outputs — reject obvious garbage."""
        return s and 4 <= len(s) <= 11

    if is_two_line:
        split = _read_two_line(plate_img)
        if is_valid_plate(split): return split
        try:
            result, _ = ocr(plate_img)
            full = _parse_det_rec(result, force_concat=True)
            if is_valid_plate(full): return full
            cand = full if _accept(full) else split
            return cand if _accept(cand) else ""
        except Exception:
            return split if _accept(split) else ""

    # single-line
    prep = prep_plate(plate_img)
    if prep is None: return ""
    fast = ""
    try:
        result, _ = ocr(prep, use_det=False, use_cls=False, use_rec=True)
        fast = _parse_rec_only(result)
        if is_valid_plate(fast): return fast
    except Exception:
        fast = ""
    try:
        result, _ = ocr(plate_img)
        full = _parse_det_rec(result)
        if is_valid_plate(full): return full
        if _accept(full) and _accept(fast):
            return full if len(full) > len(fast) else fast
        if _accept(full): return full
        if _accept(fast): return fast
        return ""
    except Exception:
        return fast if _accept(fast) else ""

def compute_flops(weights_path, input_size=(1, 3, IMG_SIZE, IMG_SIZE)):
    m = YOLO(weights_path)
    dummy = torch.randn(input_size)
    m.model.eval()
    flops, params = profile(m.model, inputs=(dummy,), verbose=False)
    return flops / 1e9, params / 1e6

def simple_cer(truths, preds):
    def lev(a, b):
        if len(a) < len(b): a, b = b, a
        if not b: return len(a)
        prev = list(range(len(b) + 1))
        for i, ca in enumerate(a):
            curr = [i + 1]
            for j, cb in enumerate(b):
                curr.append(min(prev[j+1]+1, curr[j]+1, prev[j]+(ca!=cb)))
            prev = curr
        return prev[-1]
    total = sum(len(t) for t in truths)
    errs = sum(lev(t, p) for t, p in zip(truths, preds))
    return errs / max(total, 1)

