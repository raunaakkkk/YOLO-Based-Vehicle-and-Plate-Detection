def process_video(in_path, out_path="/content/anpr_output.mp4",
                  frame_skip=2, max_frames=None):
    """
    frame_skip = N → run detection every Nth frame, reuse boxes between (speedup, smooth)
    max_frames = cap to first N frames (None = full video)
    """
    cap = cv2.VideoCapture(in_path)
    if not cap.isOpened(): raise RuntimeError(f"Cannot open {in_path}")
    fps   = cap.get(cv2.CAP_PROP_FPS) or 25.0
    W     = int(cap.get(cv2.CAP_PROP_FRAME_WIDTH))
    H     = int(cap.get(cv2.CAP_PROP_FRAME_HEIGHT))
    total = int(cap.get(cv2.CAP_PROP_FRAME_COUNT))
    if max_frames: total = min(total, max_frames)
    fourcc = cv2.VideoWriter_fourcc(*'mp4v')
    writer = cv2.VideoWriter(out_path, fourcc, fps, (W, H))

    last_result = {"detections": []}
    idx = 0
    t0 = time.perf_counter()
    while True:
        ret, frame = cap.read()
        if not ret or (max_frames and idx >= max_frames): break
        if idx % frame_skip == 0:
            last_result = detect_frame(frame)
        annotate_frame(frame, last_result)
        writer.write(frame)
        idx += 1
        if idx % 30 == 0:
            elapsed = time.perf_counter() - t0
            eta = (elapsed/idx) * (total-idx)
            print(f"  frame {idx}/{total}  elapsed={elapsed:.1f}s  eta={eta:.1f}s")
    cap.release(); writer.release()
    print(f"✓ Saved {out_path}  ({idx} frames, {time.perf_counter()-t0:.1f}s)")
    return out_path

def show_video(path):
    """Inline player in Colab."""
    mp4 = open(path, "rb").read()
    data_url = "data:video/mp4;base64," + b64encode(mp4).decode()
    return HTML(f'<video width=720 controls><source src="{data_url}" type="video/mp4"></video>')

def export_video(path, mode="download"):
    """
    mode='download' → trigger browser download (best for short videos)
    mode='drive'    → copy to Google Drive (best for long/large videos)
    mode='both'     → do both
    """
    if mode in ("download", "both"):
        try:
            files.download(path)
            print(f"✓ Browser download triggered: {path}")
        except Exception as e:
            print(f"✗ files.download failed: {e}")
            print(f"  → use Colab sidebar: 📁 Files → right-click {path} → Download")
    if mode in ("drive", "both"):
        from google.colab import drive
        import shutil
        if not os.path.ismount("/content/drive"):
            drive.mount('/content/drive')
        dest = f"/content/drive/MyDrive/{os.path.basename(path)}"
        shutil.copy(path, dest)
        print(f"✓ Saved to Drive: {dest}")

