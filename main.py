# --- 10. ROBUSTNESS ---
def robustness_retention(image_path, verbose=True):
    orig = cv2.imread(image_path)
    if orig is None: return 0.0
    def stats(res):
        confs = [p["plate_conf"] for d in res["detections"] for p in d.get("plates",[])]
        return len(confs), (sum(confs)/len(confs) if confs else 0.0)
    base_n, base_c = stats(detect_image(image_path))
    if base_n == 0:
        if verbose: print("  base found no plate; skipping")
        return 0.0
    variants = {
        "dim":   dict(alpha=0.75, beta=-20, blur=3, noise=8),
        "night": dict(alpha=0.55, beta=-40, blur=5, noise=12),
        "blur":  dict(alpha=1.00, beta=0,   blur=9, noise=5),
        "noise": dict(alpha=0.90, beta=-10, blur=3, noise=22),
    }
    scores = []
    if verbose: print("\n--- Robustness breakdown ---")
    for tag, p in variants.items():
        aug = cv2.convertScaleAbs(orig, alpha=p["alpha"], beta=p["beta"])
        if p["blur"] > 1: aug = cv2.GaussianBlur(aug, (p["blur"], p["blur"]), 0)
        if p["noise"] > 0:
            noise = np.random.normal(0, p["noise"], aug.shape).astype(np.int16)
            aug = np.clip(aug.astype(np.int16) + noise, 0, 255).astype(np.uint8)
        tmp = f"/tmp/hard_{tag}.jpg"; cv2.imwrite(tmp, aug)
        n, c = stats(detect_image(tmp))
        det_rate  = min(n / max(base_n, 1), 1.0)
        conf_rate = min(c / max(base_c, 1e-6), 1.0) if c > 0 else 0.0
        score = 0.6 * det_rate + 0.4 * conf_rate
        scores.append(score)
        if verbose: print(f"  {tag:<6} detected={n}  conf={c:.3f}  score={score:.3f}")
    return round(sum(scores)/len(scores), 3)

